    public GameWindow(String playerName, MainFrame mainFrame) {
        this.mainFrame = mainFrame;
        this.player = new Player(playerName);
        this.player.setGameWindow(this);
        this.greenhouse = new Greenhouse();
        this.greenhouse.setGameWindow(this);

        setupUI();

        growthThread = new PlantGrowthThread(greenhouse, this);
        weatherThread = new WeatherThread(greenhouse, this);

        startGameThreads();
        startGuiUpdater();
    }

    public GameWindow(String playerName, GreenhouseDatabase.SaveData saveData, MainFrame mainFrame) {
        this.mainFrame = mainFrame;
        this.player = new Player(playerName);
        this.player.setGameWindow(this);
        this.greenhouse = new Greenhouse();
        this.greenhouse.setGameWindow(this);

        this.greenhouse.setDay(saveData.day);
        this.greenhouse.addMoney(saveData.money - 1000);
        this.greenhouse.setTemperature(saveData.temperature);
        this.greenhouse.setHumidity(saveData.humidity);

        for (Plant plant : saveData.plants) {
            this.greenhouse.addPlant(plant);
        }

        this.player.getInventory().clear();
        for (String item : saveData.inventory) {
            this.player.getInventory().add(item);
        }

        setupUI();

        growthThread = new PlantGrowthThread(greenhouse, this);
        weatherThread = new WeatherThread(greenhouse, this);

        startGameThreads();
        startGuiUpdater();
    }


