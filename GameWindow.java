    private JPanel createPlantTab() {
        JPanel panel = new JPanel(new GridLayout(5, 1, 5, 10));
        panel.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10));
        
        String[] plants = {"Помидор (50 руб.)", "Огурец (40 руб.)", 
                          "Перец (70 руб.)", "Салат (30 руб.)"};
        String[] plantTypes = {"Помидор", "Огурец", "Перец", "Салат"};
        int[] costs = {50, 40, 70, 30};
        
        for (int i = 0; i < plants.length; i++) {
            final int index = i;
            JButton button = new JButton(plants[i]);
            button.addActionListener(e -> plantSeed(plantTypes[index], costs[index]));
            panel.add(button);
        }
        
        return panel;
    }
    
