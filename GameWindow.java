    private void startGameThreads() {
        growthThread.start();
        weatherThread.start();
    }
    
    private void startGuiUpdater() {
        guiUpdateTimer = new Timer(true);
        guiUpdateTimer.scheduleAtFixedRate(new TimerTask() {
            @Override
            public void run() {
                SwingUtilities.invokeLater(() -> {
                    updateGui();
                    updateInventoryDisplay();
                    checkGameOver(); // Проверяем условие поражения
                });
            }
        }, 0, 1000);
    }
    
    private void updateGui() {
        dayLabel.setText("<html><b>День:</b> " + greenhouse.getDay() + "</html>");
        moneyLabel.setText("<html><b>Деньги:</b> " + greenhouse.getMoney() + " руб.</html>");
        tempLabel.setText("<html><b>Температура:</b> " + greenhouse.getTemperature() + "°C</html>");
        humidityLabel.setText("<html><b>Влажность:</b> " + greenhouse.getHumidity() + "%</html>");
        plantCountLabel.setText("<html><b>Растений:</b> " + greenhouse.getPlantCount() + "</html>");
        
        plantListModel.clear();
        for (Plant plant : greenhouse.getPlants()) {
            String status = "";
            if (!plant.isAlive()) {
                status = "ПОГИБЛО";
            } else if (plant.isReadyToHarvest()) {
                status = "ГОТОВ К СБОРУ";
            } else if (plant.isDying()) {
                status = "КРИТИЧНОЕ СОСТОЯНИЕ";
            } else {
                status = "РАСТЕТ";
            }
            
            plantListModel.addElement(String.format("%s: рост %d%% | здоровье %d | вода %d | удобрения %d | %s",
                plant.getType(), plant.getGrowthStage(), 
                plant.getHealth(), plant.getWaterLevel(), 
                plant.getFertilizerLevel(), status));
        }
    }
    
    private void checkGameOver() {
        if (gameOver) return;
        
        if (greenhouse.getMoney() <= 0 && greenhouse.getPlants().isEmpty()) {
            gameOver = true;
            log("[ПОРАЖЕНИЕ] Вы проиграли! Закончились деньги и растения.");
            saveScore(); // Записываем рекорд
            
            int result = JOptionPane.showConfirmDialog(this,
                "Игра окончена! Ваш результат сохранен в таблице рекордов.\nВернуться в меню?",
                "Поражение",
                JOptionPane.YES_NO_OPTION);
            
            if (result == JOptionPane.YES_OPTION) {
                exitToMenu();
            }
        }
    }
    
    private void saveScore() {
        GreenhouseDatabase.saveScore(player.getName(), greenhouse, player);
        log("[РЕКОРД] Ваш результат сохранен в таблице рекордов! Счет: " + player.calculateScore());
    }
    
    public void log(String message) {
        SwingUtilities.invokeLater(() -> {
            logArea.append(message + "\n");
            logArea.setCaretPosition(logArea.getDocument().getLength());
        });
    }
    
    private void plantSeed(String plantType, int cost) {
        if (gameOver) return;

        if (player.hasSeeds(plantType)) {
            int result = JOptionPane.showConfirmDialog(this,
                "Использовать семена из инвентаря?",
                "Посадка",
                JOptionPane.YES_NO_OPTION);

            if (result == JOptionPane.YES_OPTION) {
                player.plantSeed(plantType, greenhouse);
            } else {
                buyAndPlant(plantType, cost);
            }
        } else {
            buyAndPlant(plantType, cost);
        }
        updateGui();
        updateInventoryDisplay();
    }

    private void buyAndPlant(String plantType, int cost) {
        if (gameOver) return;

        int result = JOptionPane.showConfirmDialog(this,
            "Купить семена за " + cost + " руб.?",
            "Покупка",
            JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.YES_OPTION) {
            if (player.buyItem("Семена_" + plantType, cost, greenhouse)) {
                player.plantSeed(plantType, greenhouse);
            }
        }
        updateInventoryDisplay();
    }
    
    private void waterAllPlants() {
        if (gameOver) return;
        greenhouse.waterAllPlants();
        updateGui();
    }
    
    private void fertilizeAllPlants() {
        if (gameOver) return;
        player.useFertilizer(greenhouse);
        updateGui();
        updateInventoryDisplay();
    }
    
    private void harvestAll() {
        if (gameOver) return;
        
        List<Plant> readyPlants = greenhouse.getReadyToHarvest();
        if (!readyPlants.isEmpty()) {
            int income = greenhouse.harvestAll();
            greenhouse.addMoney(income);
            player.addExperience(readyPlants.size() * 5);
        } else {
            log("[ИНФО] Нет растений готовых к сбору");
        }
        updateGui();
    }
    
    private void buyItem(String itemDesc) {
        if (gameOver) return;
        
        if (itemDesc.contains("помидора")) {
            player.buyItem("Семена_Помидор", 50, greenhouse);
        } else if (itemDesc.contains("огурца")) {
            player.buyItem("Семена_Огурец", 40, greenhouse);
        } else if (itemDesc.contains("перца")) {
            player.buyItem("Семена_Перец", 70, greenhouse);
        } else if (itemDesc.contains("салата")) {
            player.buyItem("Семена_Салат", 30, greenhouse);
        } else if (itemDesc.contains("Удобрение")) {
            player.buyItem("Удобрение", 30, greenhouse);
        } else if (itemDesc.contains("Система полива")) {
            if (greenhouse.getMoney() >= 200) {
                greenhouse.spendMoney(200);
                log("[ПОКУПКА] Установлена система полива! Растения растут быстрее.");
            }
        }
        updateGui();
        updateInventoryDisplay();
    }
    
    private void forceNextDay() {
        if (gameOver) return;
        
        log("[УСКОРЕНИЕ] Принудительный переход на следующий день");
        greenhouse.getPlants().forEach(plant -> {
            if (plant.isAlive()) {
                int currentWater = plant.getWaterLevel();
                int newWater = Math.max(0, currentWater - 10);
                plant.setWaterLevel(newWater);
                if (newWater == 0) {
                    log("[ВНИМАНИЕ] " + plant.getType() + ": вода закончилась!");
                    plant.reduceHealth(10);
                }
            }
        });
        greenhouse.getPlants().forEach(plant -> {
            if (plant.isAlive()) {
                plant.degrade();
            }
        });
        greenhouse.updatePlants();
        greenhouse.nextDay();
        log("Наступил день " + greenhouse.getDay());
        updateGui();
        updateInventoryDisplay();
    }
    
    private void saveGame() {
        if (gameOver) return;
        
        String saveName = JOptionPane.showInputDialog(this,
            "Введите название сохранения:",
            "Сохранение игры",
            JOptionPane.QUESTION_MESSAGE);
        
        if (saveName != null && !saveName.trim().isEmpty()) {
            boolean success = GreenhouseDatabase.saveGame(
                player.getName(), 
                saveName.trim(), 
                greenhouse, 
                player
            );
            
            if (success) {
                log("[СОХРАНЕНИЕ] Игра сохранена как '" + saveName + "'");
            } else {
                log("[ОШИБКА] Не удалось сохранить игру");
            }
        }
    }
    
    private void stopGame() {
        if (growthThread != null) growthThread.stopThread();
        if (weatherThread != null) weatherThread.stopThread();
        if (guiUpdateTimer != null) guiUpdateTimer.cancel();
    }
    
    private void exitToMenu() {
        if (!gameOver) {
            int result = JOptionPane.showConfirmDialog(this,
                "Сохранить игру перед выходом?",
                "Выход в меню",
                JOptionPane.YES_NO_CANCEL_OPTION);

            if (result == JOptionPane.YES_OPTION) {
                saveGame();
                saveScore();
                stopGame();
                this.dispose();
                mainFrame.setVisible(true);
            } else if (result == JOptionPane.NO_OPTION) {
                stopGame();
                this.dispose();
                mainFrame.setVisible(true);
            }
        } else {
            saveScore();
            try {
                Thread.sleep(100);
            } catch (InterruptedException ex) {}
            stopGame();
            this.dispose();
            mainFrame.setVisible(true);
        }
    }

