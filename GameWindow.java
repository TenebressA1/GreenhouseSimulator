    private JPanel createInventoryTab() {
        JPanel panel = new JPanel(new BorderLayout());
        panel.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10));
        
        inventoryListModel = new DefaultListModel<>();
        inventoryList = new JList<>(inventoryListModel);
        inventoryList.setFont(new Font("Monospaced", Font.PLAIN, 12));
        
        updateInventoryDisplay();
        
        panel.add(new JScrollPane(inventoryList), BorderLayout.CENTER);
        
        JButton useFertilizer = new JButton("Использовать удобрение");
        useFertilizer.addActionListener(e -> {
            player.useFertilizer(greenhouse);
            updateInventoryDisplay();
            updateGui();
        });
        panel.add(useFertilizer, BorderLayout.SOUTH);
        
        return panel;
    }
    
    private void updateInventoryDisplay() {
        inventoryListModel.clear();
        java.util.Map<String, Long> itemCounts = player.getInventory().stream()
            .collect(java.util.stream.Collectors.groupingBy(item -> item, java.util.stream.Collectors.counting()));
        
        itemCounts.entrySet().stream()
            .sorted((e1, e2) -> e1.getKey().compareTo(e2.getKey()))
            .forEach(entry -> {
                String item = entry.getKey();
                long count = entry.getValue();
                inventoryListModel.addElement(String.format("%s: %d шт.", item, count));
            });
        
        if (inventoryListModel.isEmpty()) {
            inventoryListModel.addElement("Инвентарь пуст");
        }
    }
    
